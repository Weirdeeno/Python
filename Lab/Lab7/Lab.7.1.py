"""
1. Описати рекурсивну функцію Fact (N) дійсного типу, яка обчислює значення
факторіала
N! = 1 · 2 · ... · N
(N> 0 - параметр цілого типу). За допомогою цієї функції обчислити факторіали
п'яти даних чисел.
"""


def Fact (N):
    if N == 0:
        return 1
    return Fact(N - 1) * N


for i in range(5):
    N = int(input('Введите число : '))
    print("Рекурсия числа = ",Fact(N))