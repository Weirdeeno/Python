'''
7. За довжинами двох сторін деякого трикутника і кутом між ними знайти довжину третьої сторони і
площу цього трикутника.
'''

import math

a = int(input("Введите длину первой стороны - "))
b = int(input("Введите длину второй стороны - "))
L = int(input("Введите угол между сторонами - "))

c = math.sqrt(a ** 2 + b ** 2 - 2 * a * b * math.cos(L))
S = 1 / 2 * a * b * math.sin(L)

print("Третья сторона треугольника = ", c)
print("Площадь треугольника = ", S)
