"""
7. З множини чисел [1..n] виділити об’єднання підмножин - чисел виду 4k+3 і
5k+2.
"""

n = int(input("Для множества чисел [1..n] \nn : "))
a = set()
b = set()
for i in range(2, n + 1, 5):
    a.add(i)
for i in range(3, n + 1, 4):
    b.add(i)

print("Объединение подножеств =", a | b)