'''
7. Описати функцію InvDigits (K), яка міняє порядок цифр цілого позитивного числа
K на зворотний (K - параметр цілого типу, який є одночасно вхідним і вихідним).
За допомогою цієї функції поміняти порядок розташування цифр на зворотний
для кожного з п'яти даних цілих чисел
'''

def InvDigits(k):
    res = int(str(k)[len(str(k)) - 1::-1])
    return res

for i in range(5):
    k = input('Введіть позитивне число : ')
    print("Число в зворотньому порядку = ",InvDigits(k))
